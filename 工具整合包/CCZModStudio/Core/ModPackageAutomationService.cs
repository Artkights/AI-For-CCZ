    private static string FindFirstScenarioPath(CczProject project, string kind)
    {
        var rsRoot = Path.Combine(project.GameRoot, "RS");
        var prefix = kind.StartsWith("S", StringComparison.OrdinalIgnoreCase) ? "S" : "R";
        return ExistingScenarioPath(rsRoot, prefix) ?? Path.Combine("RS", prefix + "_00.eex");
    }

    private static string? ExistingScenarioPath(string rsRoot, string prefix)
    {
        if (!Directory.Exists(rsRoot)) return null;
        var file = Directory.GetFiles(rsRoot, prefix + "_*.eex", SearchOption.TopDirectoryOnly).OrderBy(path => path, StringComparer.OrdinalIgnoreCase).FirstOrDefault();
        return file == null ? null : Path.Combine("RS", Path.GetFileName(file));
    }

