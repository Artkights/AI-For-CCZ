    public ModDesignAnalysisResult AnalyzeRequest(CczProject project, string prompt, string? name = null, string? packageId = null)
    {
        prompt = string.IsNullOrWhiteSpace(prompt) ? "Create a CCZ 6.5 mod package." : prompt.Trim();
        var inferredName = TrimGbk(FirstNonEmpty(name, ExtractQuoted(prompt), ExtractAfterAny(prompt, "name", "title", "mod"), "Auto Mod"), 16);
        var theme = InferTheme(prompt);
        var roles = InferRoles(prompt, theme).ToList();
        var result = new ModDesignAnalysisResult
        {
            ProjectRoot = project.GameRoot,
            Design = new ModDesign
            {
                DesignId = MakeSafeFileStem(FirstNonEmpty(packageId, inferredName, "auto-mod")),
                SourcePrompt = prompt,
                Name = inferredName,
                Theme = theme,
                TargetVersion = "6.5",
                StorySynopsis = BuildSynopsis(prompt, theme),
                Roles = roles,
                Factions = roles.Select(role => role.Faction).Where(value => !string.IsNullOrWhiteSpace(value)).Distinct(StringComparer.OrdinalIgnoreCase).ToList(),
                ResourceStyle = InferResourceStyle(prompt),
                GameplayGoals = InferGameplayGoals(prompt),
                RequestedResources = InferRequestedResources(prompt),
                BattlePlan = new ModDesignBattlePlan
                {
                    MapTheme = InferMapTheme(prompt),
                    Objective = InferObjective(prompt),
                    Tempo = prompt.Contains("fast", StringComparison.OrdinalIgnoreCase) ? "fast pressure" : "steady advance",
                    AllyCount = Math.Clamp(roles.Count, 4, 8),
                    EnemyCount = prompt.Contains("large", StringComparison.OrdinalIgnoreCase) ? 10 : 8,
                    WinConditions = { "Defeat the enemy commander.", "Keep at least one allied unit alive." },
                    LoseConditions = { "Main character retreats.", "Turn limit expires." }
                },
                Assumptions =
                {
                    ["automation"] = "Missing author inputs were filled with conservative defaults.",
                    ["scope"] = "V1 ModPackage draft with table, effect, resource, and R/S scenario plans.",
                    ["target"] = "CCZ 6.5 table/resource layout."
                }
            },
            Decisions =
            {
                "Generated a complete ModDesign from the prompt.",
                "Reserved validation for preview/apply phases.",
                "Kept generated resources as tasks unless explicit replacement paths are supplied."
            }
        };

        if (string.IsNullOrWhiteSpace(name)) result.MissingInputsAutoFilled.Add("name");
        if (roles.Count == 0) result.MissingInputsAutoFilled.Add("roles");
        return result;
    }

    public ModPackageCompileResult CompilePackage(CczProject project, IReadOnlyList<HexTableDefinition> tables, ModDesign design, int slotLimit = 32)
    {
        ArgumentNullException.ThrowIfNull(design);
        var slots = ListAvailableSlots(project, tables, slotLimit);
        var engine = _engineProfiles.Detect(project);
        var packageId = MakeSafeFileStem(FirstNonEmpty(design.DesignId, design.Name, "auto-mod"));
        var package = new ModPackage
        {
            Metadata = new ModPackageMetadata
            {
                PackageId = packageId,
                Name = FirstNonEmpty(design.Name, "Auto Mod"),
                Theme = FirstNonEmpty(design.Theme, "Classic CCZ"),
                TargetVersion = FirstNonEmpty(design.TargetVersion, engine.TableVersionPrefix, "6.5"),
                AuthorNote = "Generated by compile_mod_package.",
                SourcePrompt = design.SourcePrompt
            },
            ValidationPlan = new ModValidationPlan
            {
                RequirePreview = true,
                RequireTestCopy = true,
                RequireReread = true,
                RequireRuntimeSmoke = false,
                SmokeCommands = DefaultSmokeCommands.ToList(),
                ManualChecks =
                {
                    "Open target R/S scripts and confirm generated text/deployment flow.",
                    "Review table, effect, and resource previews before applying to the base project."
                }
            }
        };

        var roles = design.Roles.Count == 0 ? InferRoles(design.SourcePrompt, design.Theme) : design.Roles;
        var personIds = PickIds(slots, "person", roles.Count);
        var jobIds = PickIds(slots, "job_detail", Math.Min(roles.Count, 4));
        var itemIds = PickIds(slots, "item", Math.Min(roles.Count, 4));
        var effectIds = PickIds(slots, "item_effect", 2);
        var faceIds = PickImageIds(slots, "Face", roles.Count);
        var rImageIds = PickImageIds(slots, "Pmapobj", roles.Count).Select(id => Math.Max(0, (id - 1) / 2)).Distinct().Take(roles.Count).ToList();
        var sImageIds = PickImageIds(slots, "Unit_mov", roles.Count);
        var rScenario = PickKeys(slots, "r_scenario", 1).FirstOrDefault() ?? "R_99";
        var sScenario = PickKeys(slots, "s_scenario", 1).FirstOrDefault() ?? "S_99";

        package.SlotPlan.PersonIds.AddRange(personIds);
        package.SlotPlan.JobIds.AddRange(jobIds);
        package.SlotPlan.ItemIds.AddRange(itemIds);
        package.SlotPlan.EffectIds.AddRange(effectIds);
        package.SlotPlan.FaceImageNumbers.AddRange(faceIds);
        package.SlotPlan.RImageIds.AddRange(rImageIds);
        package.SlotPlan.SImageIds.AddRange(sImageIds);
        package.SlotPlan.ScenarioIds.AddRange(new[] { rScenario, sScenario });
        package.SlotPlan.Notes["selection"] = "Slots were selected from conservative available-slot scans.";
        package.SlotPlan.Notes["automation_mode"] = "aggressive_test_copy";

        AddRoleTableUpdates(project, tables, package, engine.TableHints.PersonTable, roles, personIds);
        AddJobTableUpdates(project, tables, package, engine.TableHints.DetailedJobTable, roles, jobIds);
        AddItemTableUpdates(project, tables, package, itemIds, design);
        AddEffectPackages(package, effectIds, itemIds, design);
        AddScenarioDrafts(project, package, design, rScenario, sScenario);
        AddResourcePlans(package, roles, faceIds, rImageIds, sImageIds, design);

        var result = new ModPackageCompileResult
        {
            ProjectRoot = project.GameRoot,
            Design = design,
            Slots = slots,
            Package = package,
            Decisions =
            {
                $"personIds: {string.Join(", ", personIds)}",
                $"jobIds: {string.Join(", ", jobIds)}",
                $"itemIds: {string.Join(", ", itemIds)}",
                $"effectIds: {string.Join(", ", effectIds)}",
                $"R/S: {string.Join(", ", package.SlotPlan.ScenarioIds)}"
            }
        };

        if (package.TableUpdates.Count == 0) result.Warnings.Add("No table updates were generated; inspect slotPlan before applying.");
        if (package.ResourceUpdates.Count == 0) result.Warnings.Add("No resource tasks were generated.");
        return result;
    }

    public ModPackageRepairResult RepairPackage(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        ModPackage package,
        ModPackagePreviewResult preview,
        ModAvailableSlotsResult? slots = null,
        SceneStringDocument? dictionary = null)
    {
        var json = JsonSerializer.Serialize(package);
        var repaired = JsonSerializer.Deserialize<ModPackage>(json) ?? package;
        var result = new ModPackageRepairResult
        {
            ProjectRoot = project.GameRoot,
            PackageId = NormalizePackageId(repaired),
            Package = repaired
        };

        slots ??= ListAvailableSlots(project, tables, 64);
        RepairDuplicateSlots(repaired.SlotPlan.PersonIds, PickIds(slots, "person", 64), "personIds", result);
        RepairDuplicateSlots(repaired.SlotPlan.JobIds, PickIds(slots, "job_detail", 64), "jobIds", result);
        RepairDuplicateSlots(repaired.SlotPlan.ItemIds, PickIds(slots, "item", 64), "itemIds", result);
        RepairDuplicateSlots(repaired.SlotPlan.EffectIds, PickIds(slots, "item_effect", 64), "effectIds", result);

        foreach (var issue in preview.Issues.Where(issue => issue.Message.Contains("GBK byte length", StringComparison.OrdinalIgnoreCase)))
        {
            RepairGbkOverflow(project, tables, repaired, issue, result);
        }

        foreach (var issue in preview.Issues.Where(issue => issue.Category.Equals("resource", StringComparison.OrdinalIgnoreCase)))
        {
            var before = repaired.ResourceUpdates.Count;
            repaired.ResourceUpdates.RemoveAll(update =>
                update.TargetRelativePath.Equals(issue.Target, StringComparison.OrdinalIgnoreCase) ||
                issue.Target.Contains(update.TargetRelativePath, StringComparison.OrdinalIgnoreCase));
            if (repaired.ResourceUpdates.Count != before)
            {
                result.Changed = true;
                result.Repairs.Add("Removed invalid resource update: " + issue.Target);
            }
        }

        var repairedPreview = Preview(project, tables, repaired, dictionary, allowStructuralScenarioWrites: true);
        result.RemainingIssues = repairedPreview.Issues.Where(issue => IsBlocking(issue.Severity)).ToList();
        return result;
    }

    public ScenarioPatchCompileResult CompileScenarioPatch(CczProject project, ScenarioPatchCompileRequest request, SceneStringDocument dictionary)
    {
        var relativePath = string.IsNullOrWhiteSpace(request.RelativePath)
            ? FindFirstScenarioPath(project, request.Kind)
            : request.RelativePath;
        var story = TrimGbk(FirstNonEmpty(request.StoryText, "Generated story text."), 180);
        var objective = TrimGbk(FirstNonEmpty(request.BattleObjective, "Generated battle objective."), 180);
        var operation = new ModScenarioPatchOperation
        {
            Operation = request.InsertMode.Equals("insert", StringComparison.OrdinalIgnoreCase) ? "insert_command" : "append_command",
            SceneIndex = request.SceneIndex <= 0 ? 1 : request.SceneIndex,
            SectionIndex = request.SectionIndex <= 0 ? 1 : request.SectionIndex,
            Note = "Compiled scenario text block."
        };
        operation.Commands.Add(TextCommand(0x14, story));
        operation.Commands.Add(TextCommand(0x19, objective));

        var patch = new ModScenarioPatch
        {
            PatchId = Path.GetFileNameWithoutExtension(relativePath) + "-compiled",
            RelativePath = relativePath,
            Note = "Compiled scenario patch.",
            Operations = { operation }
        };

        return new ScenarioPatchCompileResult
        {
            ProjectRoot = project.GameRoot,
            Patch = patch,
            Preview = PreviewScenarioPatch(project, patch, dictionary, allowStructuralScenarioWrites: true),
            Decisions =
            {
                "Selected target: " + relativePath,
                "Generated two text commands.",
                "Preview allows structural append/insert for validation."
            }
        };
    }

    public ScenarioPatchApplyResult ApplyScenarioPatchAggressive(
        CczProject project,
        ModScenarioPatch patch,
        SceneStringDocument dictionary,
        bool forceOpenScenarioWrites = false)
    {
        var preview = PreviewScenarioPatch(project, patch, dictionary, allowStructuralScenarioWrites: true, forceOpenScenarioWrites);
        if (!preview.CanApply)
        {
            return new ScenarioPatchApplyResult
            {
                PatchId = preview.PatchId,
                RelativePath = preview.RelativePath,
                Applied = false,
                Issues = preview.Issues,
                Changes = preview.Changes
            };
        }

        if (patch.Operations.Any(operation => !WritableScenarioOperations.Contains(operation.Operation)))
        {
            preview.Issues.Add(Issue("blocked", "scenario", preview.RelativePath, "Structural scenario append/insert is preview-only in this conservative build."));
            return new ScenarioPatchApplyResult
            {
                PatchId = preview.PatchId,
                RelativePath = preview.RelativePath,
                Applied = false,
                Issues = preview.Issues,
                Changes = preview.Changes
            };
        }

        return ApplyScenarioPatch(project, patch, dictionary);
    }

    public ModAutoValidateResult AutoValidate(CczProject project, IReadOnlyList<HexTableDefinition> tables, ModPackage package, SceneStringDocument? dictionary, bool runSmokes, string? smokeProjectPath = null)
    {
        var preview = Preview(project, tables, package, dictionary, allowStructuralScenarioWrites: IsForceOpenPackage(package));
        var result = new ModAutoValidateResult
        {
            ProjectRoot = project.GameRoot,
            PackageId = NormalizePackageId(package),
            Preview = preview,
            Issues = preview.Issues.ToList()
        };

        foreach (var smoke in preview.RequiredSmokeCommands)
        {
            result.SmokeRuns.Add(runSmokes ? RunSmokeCommand(project, smoke, smokeProjectPath) : new ModSmokeRunResult
            {
                Command = smoke,
                Ran = false,
                Passed = IsForceOpenPackage(package),
                Error = runSmokes ? string.Empty : "Smoke execution was not requested."
            });
        }

        result.Passed = preview.CanApply && (IsForceOpenPackage(package) || result.SmokeRuns.All(run => run.Passed));
        result.Summary = $"auto_validate_mod: staticPassed={preview.CanApply}, smokeRuns={result.SmokeRuns.Count}, issues={result.Issues.Count}.";
        result.ReportPath = WriteAutoValidationReport(project, package, result);
        return result;
    }

    internal static void PreviewStructuralScenarioOperation(
        LegacyScenarioDocument document,
        ModScenarioPatchOperation operation,
        ScenarioPatchPreviewResult preview)
    {
        var section = ResolveScenarioSection(document, operation);
        if (section == null)
        {
            preview.Issues.Add(Issue("error", "scenario", preview.RelativePath, "Target scene/section was not found."));
            return;
        }

        var insertIndex = ResolveInsertIndex(section, operation);
        foreach (var draft in operation.Commands)
        {
            preview.Changes.Add(new ModPackageChangePreview
            {
                Category = "scenario",
                Target = preview.RelativePath,
                RowId = insertIndex,
                Field = operation.Operation,
                OldValue = "existing command stream",
                NewValue = BuildDraftSummary(draft),
                Changed = true,
                Note = operation.Note
            });
            insertIndex++;
        }
    }

    private static LegacyScenarioSection? ResolveScenarioSection(LegacyScenarioDocument document, ModScenarioPatchOperation operation)
    {
        var sceneIndex = operation.SceneIndex ?? 1;
        var sectionIndex = operation.SectionIndex ?? 1;
        return document.Scenes.FirstOrDefault(scene => scene.SceneIndex == sceneIndex)
            ?.Sections.FirstOrDefault(section => section.SectionIndex == sectionIndex);
    }

    private static int ResolveInsertIndex(LegacyScenarioSection section, ModScenarioPatchOperation operation)
    {
        if (operation.InsertBeforeCommandIndex.HasValue)
        {
            return Math.Clamp(operation.InsertBeforeCommandIndex.Value, 0, section.Commands.Count);
        }

        if (operation.InsertAfterCommandIndex.HasValue)
        {
            return Math.Clamp(operation.InsertAfterCommandIndex.Value + 1, 0, section.Commands.Count);
        }

        return section.Commands.Count;
    }

    private static ModScenarioCommandDraft TextCommand(int commandId, string text)
        => new()
        {
            CommandId = commandId,
            Parameters =
            {
                new ModScenarioParameterDraft
                {
                    LayoutCode = 0x05,
                    Kind = "text",
                    Text = TrimGbk(text, 180)
                }
            }
        };

    private static string BuildDraftSummary(ModScenarioCommandDraft draft)
    {
        var commandId = draft.CommandId != 0 ? draft.CommandId : ResolveCommandId(draft.CommandId, draft.CommandIdHex);
        var parameters = draft.Parameters.Count == 0 ? "no params" : draft.Parameters.Count.ToString(CultureInfo.InvariantCulture) + " params";
        return $"command {HexDisplayFormatter.FormatByte((byte)Math.Clamp(commandId, 0, 255))} ({parameters})";
    }

    private static IReadOnlyList<int> PickIds(ModAvailableSlotsResult slots, string kind, int count)
        => slots.Groups
            .Where(group => group.Kind.Equals(kind, StringComparison.OrdinalIgnoreCase))
            .SelectMany(group => group.AvailableIds)
            .Distinct()
            .OrderBy(id => id)
            .Take(Math.Max(0, count))
            .ToList();

    private static IReadOnlyList<int> PickImageIds(ModAvailableSlotsResult slots, string sourceContains, int count)
        => slots.Groups
            .Where(group => group.Kind.Equals("image_resource", StringComparison.OrdinalIgnoreCase) &&
                            group.Source.Contains(sourceContains, StringComparison.OrdinalIgnoreCase))
            .SelectMany(group => group.AvailableIds)
            .Distinct()
            .OrderBy(id => id)
            .Take(Math.Max(0, count))
            .ToList();

    private static IReadOnlyList<string> PickKeys(ModAvailableSlotsResult slots, string kind, int count)
        => slots.Groups
            .Where(group => group.Kind.Equals(kind, StringComparison.OrdinalIgnoreCase))
            .SelectMany(group => group.AvailableKeys)
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .Take(Math.Max(0, count))
            .ToList();

    private void AddRoleTableUpdates(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        ModPackage package,
        string tableName,
        IReadOnlyList<ModDesignRole> roles,
        IReadOnlyList<int> personIds)
    {
        if (!TryFindWritableNameColumn(project, tables, tableName, out var table, out var column, out var capacity)) return;
        for (var i = 0; i < personIds.Count; i++)
        {
            var role = roles.Count > i ? roles[i] : null;
            package.TableUpdates.Add(new ModTableUpdate
            {
                TableName = table.TableName,
                RowId = personIds[i],
                Values = { [column] = JsonSerializer.SerializeToElement(TrimGbk(FirstNonEmpty(role?.Name, "Role" + (i + 1).ToString(CultureInfo.InvariantCulture)), capacity)) },
                Note = "Generated role name."
            });
        }
    }

    private void AddJobTableUpdates(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        ModPackage package,
        string tableName,
        IReadOnlyList<ModDesignRole> roles,
        IReadOnlyList<int> jobIds)
    {
        if (!TryFindWritableNameColumn(project, tables, tableName, out var table, out var column, out var capacity)) return;
        for (var i = 0; i < jobIds.Count; i++)
        {
            var role = roles.Count > i ? roles[i] : null;
            package.TableUpdates.Add(new ModTableUpdate
            {
                TableName = table.TableName,
                RowId = jobIds[i],
                Values = { [column] = JsonSerializer.SerializeToElement(TrimGbk(FirstNonEmpty(role?.Job, "Job" + (i + 1).ToString(CultureInfo.InvariantCulture)), capacity)) },
                Note = "Generated job name."
            });
        }
    }

    private void AddItemTableUpdates(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        ModPackage package,
        IReadOnlyList<int> itemIds,
        ModDesign design)
    {
        var itemTables = HexTableNameResolver.ResolveItemTables(project, tables);
        var index = 0;
        foreach (var itemTable in itemTables)
        {
            if (!TryFindWritableNameColumn(project, tables, itemTable.TableName, out var table, out var column, out var capacity)) continue;
            foreach (var itemId in itemIds.Skip(index).Take(Math.Max(0, itemIds.Count - index)))
            {
                if (itemId < table.BeginId || itemId >= table.BeginId + table.RowCount) continue;
                package.TableUpdates.Add(new ModTableUpdate
                {
                    TableName = table.TableName,
                    RowId = itemId,
                    Values = { [column] = JsonSerializer.SerializeToElement(TrimGbk(FirstNonEmpty(design.Theme, "Mod Item"), capacity)) },
                    Note = "Generated item name."
                });
                index++;
                break;
            }
        }
    }

    private static void AddEffectPackages(ModPackage package, IReadOnlyList<int> effectIds, IReadOnlyList<int> itemIds, ModDesign design)
