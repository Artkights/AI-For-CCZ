        package.EffectPackages.Add(new EffectPackage
        {
            PackageId = package.Metadata.PackageId + "-item-effect",
            Domain = "item",
            EffectId = effectIds[0],
            Name = TrimGbk(FirstNonEmpty(design.Theme, "Mod Effect"), 16),
            Description = TrimGbk("Generated item effect for " + FirstNonEmpty(design.BattlePlan.Objective, "battle objective"), 64),
            EffectValue = 10,
            Bindings =
            {
                new EffectPackageBinding
                {
                    Kind = "item",
                    RowId = firstItem,
                    ItemId = firstItem,
                    EffectValue = 10,
                    Note = "Generated by compile_mod_package."
                }
            },
            SourcePrompt = design.SourcePrompt,
            BackupNote = "Generated ModPackage effect."
        });

        if (effectIds.Count < 2) return;
        var secondItem = itemIds.Skip(1).FirstOrDefault(firstItem);
        package.EffectPackages.Add(new EffectPackage
        {
            PackageId = package.Metadata.PackageId + "-bonus-effect",
            Domain = "item",
            EffectId = effectIds[1],
            Name = TrimGbk("Bonus Effect", 16),
            Description = TrimGbk("Generated secondary item effect.", 64),
            EffectValue = 5,
            Bindings =
            {
                new EffectPackageBinding
                {
                    Kind = "item",
                    RowId = secondItem,
                    ItemId = secondItem,
                    EffectValue = 5,
                    Note = "Generated by compile_mod_package."
                }
            },
            SourcePrompt = design.SourcePrompt,
            BackupNote = "Generated ModPackage effect."
        });
    }

