    private static string ExtractQuoted(string text)
    {
        foreach (var pair in new[] { ('"', '"'), ('\'', '\'') })
        {
            var start = text.IndexOf(pair.Item1);
            if (start < 0) continue;
            var end = text.IndexOf(pair.Item2, start + 1);
            if (end > start) return text[(start + 1)..end].Trim();
        }

        return string.Empty;
    }

    private static string ExtractAfterAny(string text, params string[] markers)
    {
        foreach (var marker in markers)
        {
            var index = text.IndexOf(marker, StringComparison.OrdinalIgnoreCase);
            if (index < 0) continue;
            var tail = text[(index + marker.Length)..].Trim(' ', ':', '-', ',', ';');
            if (tail.Length == 0) continue;
            var stop = tail.IndexOfAny([';', '\r', '\n']);
            return stop > 0 ? tail[..stop].Trim() : tail.Trim();
        }

        return string.Empty;
    }

    private static string FirstNonEmpty(params string?[] values)
        => values.FirstOrDefault(value => !string.IsNullOrWhiteSpace(value))?.Trim() ?? string.Empty;

    private static string TrimGbk(string value, int maxBytes)
    {
        if (maxBytes <= 0) return string.Empty;
        value = value.Trim();
        if (EncodingService.GetGbkByteCount(value) <= maxBytes) return value;
        var builder = new StringBuilder();
        foreach (var ch in value)
        {
            var next = builder.ToString() + ch;
            if (EncodingService.GetGbkByteCount(next) > maxBytes) break;
            builder.Append(ch);
        }

        return builder.ToString();
    }

    private static string Tail(string value, int maxChars)
        => value.Length <= maxChars ? value : value[^maxChars..];
