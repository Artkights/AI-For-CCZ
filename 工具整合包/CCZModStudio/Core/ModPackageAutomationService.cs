    private static IReadOnlyList<ModDesignRole> InferRoles(string prompt, string theme)
    {
        var names = ExtractNames(prompt);
        while (names.Count < 4)
        {
            names.Add(new[] { "Hero", "Strategist", "Archer", "Medic", "Guard", "Raider", "Captain", "Officer" }[names.Count]);
        }

        return names.Take(8).Select((name, index) => new ModDesignRole
        {
            Name = TrimGbk(name, 16),
            Faction = index == 0 ? "ally" : index < 4 ? "friendly" : "enemy",
            Job = index switch
            {
                0 => theme.Contains("Fire", StringComparison.OrdinalIgnoreCase) ? "Vanguard" : "Commander",
                1 => "Strategist",
                2 => "Archer",
                3 => "Medic",
                _ => "Enemy"
            },
            Personality = index == 0 ? "bold" : "pragmatic",
            GameplayRole = index switch
            {
                0 => "frontline leader",
                1 => "support strategist",
                2 => "ranged pressure",
                3 => "healer",
                _ => "enemy unit"
            }
        }).ToList();
    }

    private static List<string> ExtractNames(string prompt)
    {
        var result = new List<string>();
        foreach (var separator in new[] { "roles:", "names:", "characters:" })
        {
            var index = prompt.IndexOf(separator, StringComparison.OrdinalIgnoreCase);
            if (index < 0) continue;
            var tail = prompt[(index + separator.Length)..];
            foreach (var part in tail.Split(new[] { ',', ';', '/', ' ', '\r', '\n' }, StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries))
            {
                var cleaned = new string(part.Where(ch => char.IsLetterOrDigit(ch) || ch >= 0x4E00 && ch <= 0x9FFF).ToArray());
                if (cleaned.Length is >= 2 and <= 16 && !result.Contains(cleaned, StringComparer.OrdinalIgnoreCase))
                {
                    result.Add(cleaned);
                }

                if (result.Count >= 8) return result;
            }
        }

        return result;
    }

