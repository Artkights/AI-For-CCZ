    private static void AddResourcePlans(
        ModPackage package,
        IReadOnlyList<ModDesignRole> roles,
        IReadOnlyList<int> faceIds,
        IReadOnlyList<int> rImageIds,
        IReadOnlyList<int> sImageIds,
        ModDesign design)
    {
        package.SlotPlan.Notes["resource_style"] = FirstNonEmpty(design.ResourceStyle, "CCZ 6.5 compatible placeholder style.");
        for (var i = 0; i < roles.Count; i++)
        {
            var roleName = FirstNonEmpty(roles[i].Name, "Role" + (i + 1).ToString(CultureInfo.InvariantCulture));
            if (i < faceIds.Count) package.SlotPlan.Notes[$"face:{roleName}"] = $"Face.e5 #{faceIds[i]}";
            if (i < rImageIds.Count) package.SlotPlan.Notes[$"r_actor:{roleName}"] = $"Pmapobj R={rImageIds[i]}";
            if (i < sImageIds.Count) package.SlotPlan.Notes[$"s_unit:{roleName}"] = $"Unit S={sImageIds[i]}";
        }
    }

    private bool TryFindWritableNameColumn(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        string tableName,
        out HexTableDefinition table,
        out string columnName,
        out int capacity)
    {
        table = null!;
        columnName = string.Empty;
        capacity = 0;
        try
        {
            table = HexTableNameResolver.ResolveForProject(project, tables, tableName);
            var read = _tableReader.Read(project, table, tables);
            if (!read.Validation.IsUsable) return false;
            var candidates = read.Data.Columns.Cast<DataColumn>()
                .Select(column => new { Column = column, Field = column.ExtendedProperties["FieldDefinition"] as HexFieldDefinition })
                .Where(item => item.Field is { Kind: HexFieldKind.FixedString, ConsumesBytes: true })
                .ToList();
            var preferred = candidates.FirstOrDefault(item =>
                item.Column.ColumnName.Contains("name", StringComparison.OrdinalIgnoreCase) ||
                item.Column.ColumnName.Contains("Name", StringComparison.OrdinalIgnoreCase)) ?? candidates.FirstOrDefault();
            if (preferred == null) return false;
            columnName = preferred.Column.ColumnName;
            capacity = preferred.Field!.Size;
            return true;
        }
        catch
        {
            return false;
        }
    }

    private static void RepairDuplicateSlots(List<int> ids, IReadOnlyList<int> replacements, string label, ModPackageRepairResult result)
    {
        var used = new HashSet<int>();
        var replacementQueue = new Queue<int>(replacements.Where(id => id > 0));
        for (var i = 0; i < ids.Count; i++)
        {
            if (ids[i] <= 0 || used.Add(ids[i])) continue;
            int replacement;
            do
            {
                if (replacementQueue.Count == 0) return;
                replacement = replacementQueue.Dequeue();
            }
            while (!used.Add(replacement));

            var old = ids[i];
            ids[i] = replacement;
            result.Changed = true;
            result.Repairs.Add($"{label}: replaced duplicate {old} with {replacement}.");
        }
    }

    private static void RepairGbkOverflow(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        ModPackage package,
        ModPackageValidationIssue issue,
        ModPackageRepairResult result)
    {
        foreach (var update in package.TableUpdates)
        {
            foreach (var key in update.Values.Keys.ToList())
            {
                if (!issue.Target.Contains(update.TableName, StringComparison.OrdinalIgnoreCase) ||
                    !issue.Target.Contains(key, StringComparison.OrdinalIgnoreCase))
                {
                    continue;
                }

                try
                {
                    var table = HexTableNameResolver.ResolveForProject(project, tables, update.TableName);
                    var read = new HexTableReader().Read(project, table, tables);
                    var column = read.Data.Columns[key];
                    var field = column?.ExtendedProperties["FieldDefinition"] as HexFieldDefinition;
                    if (field == null || field.Kind != HexFieldKind.FixedString) continue;
                    var value = update.Values[key].ValueKind == JsonValueKind.String ? update.Values[key].GetString() ?? string.Empty : update.Values[key].ToString();
                    var shortened = TrimGbk(value, field.Size);
                    update.Values[key] = JsonSerializer.SerializeToElement(shortened);
                    result.Changed = true;
                    result.Repairs.Add($"Trimmed GBK text {update.TableName}[{update.RowId}].{key}.");
                }
                catch
                {
                    // Best-effort repair only.
                }
            }
        }
    }

    private static ModSmokeRunResult RunSmokeCommand(CczProject project, string smokeCommand, string? smokeProjectPath)
    {
        var result = new ModSmokeRunResult { Command = smokeCommand, Ran = true };
        var dll = ResolveSmokeTestsDll(project, smokeProjectPath);
        if (dll == null)
        {
            result.Passed = false;
            result.Error = "SmokeTests DLL was not found. Build CCZModStudio.SmokeTests first.";
            return result;
        }

        try
        {
            using var process = new Process();
            process.StartInfo.FileName = "dotnet";
            process.StartInfo.ArgumentList.Add(dll);
            process.StartInfo.ArgumentList.Add(smokeCommand);
            process.StartInfo.RedirectStandardOutput = true;
            process.StartInfo.RedirectStandardError = true;
            process.StartInfo.UseShellExecute = false;
            process.StartInfo.WorkingDirectory = project.WorkspaceRoot;
            process.Start();
            var output = process.StandardOutput.ReadToEnd();
            var error = process.StandardError.ReadToEnd();
            process.WaitForExit(120_000);
            result.ExitCode = process.ExitCode;
            result.Passed = process.ExitCode == 0;
            result.OutputTail = Tail(output, 4000);
            result.Error = Tail(error, 2000);
        }
        catch (Exception ex)
        {
            result.Passed = false;
            result.Error = ex.Message;
        }

        return result;
    }

    private static string? ResolveSmokeTestsDll(CczProject project, string? smokeProjectPath)
    {
        var candidates = new List<string>();
        if (!string.IsNullOrWhiteSpace(smokeProjectPath))
        {
            candidates.Add(smokeProjectPath);
        }

        candidates.Add(Path.Combine(project.WorkspaceRoot, "CCZModStudio.SmokeTests", "bin", "Debug", "net8.0-windows", "CCZModStudio.SmokeTests.dll"));
        candidates.Add(Path.Combine(project.WorkspaceRoot, "CCZModStudio.SmokeTests", "bin", "Release", "net8.0-windows", "CCZModStudio.SmokeTests.dll"));
        return candidates.Select(Path.GetFullPath).FirstOrDefault(File.Exists);
    }

    private static string WriteAutoValidationReport(CczProject project, ModPackage package, ModAutoValidateResult result)
