    public StandaloneScenarioAnalysisResult AnalyzeStandaloneScenarioRequest(
        CczProject project,
        string prompt,
        string? title = null,
        string? packageId = null)
    {
        prompt = string.IsNullOrWhiteSpace(prompt) ? "Create a standalone CCZ 6.5 single-stage scenario." : prompt.Trim();
        var inferredTitle = TrimGbk(FirstNonEmpty(title, ExtractStandaloneTitle(prompt), ExtractQuoted(prompt), ExtractAfterAny(prompt, "title", "name"), "Standalone Stage"), 16);
        var theme = InferStandaloneTheme(prompt);
        var design = new StandaloneScenarioDesign
        {
            DesignId = MakeSafeFileStem(FirstNonEmpty(packageId, inferredTitle, "standalone-stage")),
            SourcePrompt = prompt,
            Title = inferredTitle,
            Theme = theme,
            TargetVersion = "6.5",
            Synopsis = TrimGbk(FirstNonEmpty(ExtractAfterAny(prompt, "story", "synopsis"), $"A complete standalone {theme} stage with intro, deployment, battle events, victory, defeat, and epilogue."), 180),
            RScenarioId = "R_AUTO",
            SScenarioId = "S_AUTO",
            Rewards = { "Bonus gold", "Rare item", "Story unlock" },
            Assumptions =
            {
                ["scope"] = "standalone_single_stage",
                ["constraint_mode"] = "force_open",
                ["rs_event_dsl"] = "typed_event_graph",
                ["write_policy"] = "Structure commands are generated for preview/report; direct writes still require preview."
            }
        };

        AddStandaloneRoles(design);
        AddStandaloneBattlePlan(design, prompt);
        AddStandaloneEventGraph(design);
        AddStandaloneResourcePlan(design);

        return new StandaloneScenarioAnalysisResult
        {
            ProjectRoot = project.GameRoot,
            Design = design,
            Decisions =
            {
                "Expanded prompt into a standalone R+S design.",
                "Applied force_open constraints for complete single-stage output.",
                "Generated default roles, battle deployment, event graph, rewards, and resource needs."
            }
        };
    }

    public StandaloneScenarioCompileResult CompileStandaloneScenarioPackage(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        StandaloneScenarioDesign design,
        int slotLimit = 64)
    {
        ArgumentNullException.ThrowIfNull(design);
        if (design.Roles.Count == 0) AddStandaloneRoles(design);
        if (design.Battle.Units.Count == 0) AddStandaloneBattlePlan(design, design.SourcePrompt);
        if (design.EventGraph.Nodes.Count == 0) AddStandaloneEventGraph(design);
        if (design.Resources.Needs.Count == 0) AddStandaloneResourcePlan(design);

        var slots = ListAvailableSlots(project, tables, slotLimit);
        BindStandaloneSlots(slots, design);
        var packageId = MakeSafeFileStem(FirstNonEmpty(design.DesignId, design.Title, "standalone-stage"));
        var package = new ModPackage
        {
            Metadata = new ModPackageMetadata
            {
                PackageId = packageId,
                Name = FirstNonEmpty(design.Title, "Standalone Stage"),
                Theme = FirstNonEmpty(design.Theme, "Classic CCZ"),
                TargetVersion = FirstNonEmpty(design.TargetVersion, "6.5"),
                AuthorNote = "Generated by compile_standalone_scenario_package.",
                SourcePrompt = design.SourcePrompt,
                Tags =
                {
                    ["scope"] = "standalone_single_stage",
                    ["constraint_mode"] = "force_open"
                }
            },
            ValidationPlan = new ModValidationPlan
            {
                RequirePreview = true,
                RequireTestCopy = false,
                RequireReread = true,
                RequireRuntimeSmoke = true,
                SmokeCommands =
                {
                    "--standalone-scenario-smoke",
                    "--standalone-semantic-smoke",
                    "--standalone-golden-samples-smoke",
                    "--mod-package-smoke",
                    "--rs-text-write-smoke",
                    "--rs-deployment-write-smoke",
                    "--rs-write-smoke",
                    "--hexzmap-sync-smoke",
                    "--map-terrain-consistency-smoke",
                    "--strict-playable-preview-smoke"
                },
                ManualChecks =
                {
                    "Playability: R intro can open.",
                    "Playability: R intro contains an S jump plan.",
                    "Playability: S battle has deployment, victory, defeat, battle events, and epilogue.",
                    "Force-open: validation failures are reported but do not reduce output to text-only."
                }
            }
        };

        package.SlotPlan.PersonIds.AddRange(design.Roles.Select(role => role.PersonId).Where(id => id > 0));
        package.SlotPlan.JobIds.AddRange(design.Roles.Select(role => role.JobId).Where(id => id > 0).Distinct());
        package.SlotPlan.ItemIds.AddRange(PickIds(slots, "item", 2));
        package.SlotPlan.EffectIds.AddRange(PickIds(slots, "item_effect", 2));
        package.SlotPlan.FaceImageNumbers.AddRange(design.Roles.Select(role => role.FaceImageNumber).Where(id => id > 0));
        package.SlotPlan.RImageIds.AddRange(design.Roles.Select(role => role.RImageId).Where(id => id > 0));
        package.SlotPlan.SImageIds.AddRange(design.Roles.Select(role => role.SImageId).Where(id => id > 0));
        package.SlotPlan.ScenarioIds.Add(FirstNonEmpty(design.RScenarioId, "R_AUTO"));
        package.SlotPlan.ScenarioIds.Add(FirstNonEmpty(design.SScenarioId, "S_AUTO"));
        package.SlotPlan.MapIds.Add(FirstNonEmpty(design.Battle.MapId, "M000"));
        package.SlotPlan.Notes["standalone_title"] = design.Title;
        package.SlotPlan.Notes["event_nodes"] = design.EventGraph.Nodes.Count.ToString(CultureInfo.InvariantCulture);
        package.SlotPlan.Notes["deployment_units"] = design.Battle.Units.Count.ToString(CultureInfo.InvariantCulture);
        package.SlotPlan.Notes["resource_needs"] = string.Join("; ", design.Resources.Needs.Select(need => $"{need.Kind}:{need.Target}"));

        AddStandaloneTableUpdates(project, tables, package, design);
        AddStandaloneEffectPackages(package, design);
        AddStandaloneScenarioPatches(project, package, design);
        AddStandaloneResourcePlans(package, design);

        var playability = BuildStandalonePlayabilityChecklist(design, package);
        return new StandaloneScenarioCompileResult
        {
            ProjectRoot = project.GameRoot,
            Design = design,
            Slots = slots,
            Package = package,
            Playability = playability,
            Decisions =
            {
                $"roles: {design.Roles.Count}",
                $"deployment units: {design.Battle.Units.Count}",
                $"event nodes: {design.EventGraph.Nodes.Count}",
                $"R/S: {string.Join(", ", package.SlotPlan.ScenarioIds)}",
                "force_open keeps structural scenario output visible for preview/report."
            }
        };
    }

    private static void AddStandaloneRoles(StandaloneScenarioDesign design)
    {
        if (design.Roles.Count > 0) return;
        var names = ExtractStandaloneRoleNames(design.SourcePrompt);
        var defaults = new[] { "Hero", "Strategist", "Archer", "Medic", "Guest", "Boss", "Enemy1", "Enemy2", "Enemy3", "Enemy4", "Enemy5", "Enemy6", "Enemy7", "Enemy8" };
        while (names.Count < defaults.Length) names.Add(defaults[names.Count]);
        var jobs = new[] { "Commander", "Strategist", "Archer", "Medic", "Guest", "Boss", "Guard", "Archer", "Tactician", "Cavalry", "Infantry", "Mage", "Captain", "Guard" };
        var factions = InferStandaloneFactions(design.SourcePrompt, names.Count);
        for (var i = 0; i < 14; i++)
        {
            design.Roles.Add(new StandaloneScenarioRole
            {
                Key = i switch
                {
                    0 => "hero",
                    1 => "strategist",
                    2 => "archer",
                    3 => "healer",
                    4 => "guest",
                    5 => "boss",
                    _ => "enemy" + (i - 5).ToString(CultureInfo.InvariantCulture)
                },
                Name = TrimGbk(names[i], 16),
                Faction = i < factions.Count ? factions[i] : i < 4 ? "ally" : i == 4 ? "friendly" : "enemy",
                Job = jobs[i],
                Personality = i switch
                {
                    0 => "bold",
                    1 => "calm",
                    2 => "precise",
                    3 => "careful",
                    4 => "loyal",
                    5 => "proud",
                    _ => "disciplined"
                },
                GameplayRole = i switch
                {
                    0 => "frontline leader",
                    1 => "support strategist",
                    2 => "ranged pressure",
                    3 => "healer",
                    4 => "guest ally",
                    5 => "enemy commander",
                    _ => "enemy unit"
                },
                Level = i < 4 ? 12 : i == 4 ? 10 : i == 5 ? 16 : 11
            });
        }
    }

    private static void AddStandaloneBattlePlan(StandaloneScenarioDesign design, string prompt)
    {
        design.Battle.MapTheme = InferStandaloneMapTheme(prompt);
        design.Battle.MapId = InferStandaloneMapId(prompt);
        design.Battle.Objective = FirstNonEmpty(ExtractAfterAny(prompt, "objective", "win", "目标", "胜利"), InferStandaloneObjective(prompt));
        design.Battle.Tempo = prompt.Contains("fast", StringComparison.OrdinalIgnoreCase) ? "fast pressure" : "steady advance";
        design.Battle.TurnLimit = prompt.Contains("fast", StringComparison.OrdinalIgnoreCase) ? 18 : 24;
        design.Battle.WinConditions.AddRange(new[] { design.Battle.Objective, "At least one allied unit survives." });
        design.Battle.LoseConditions.AddRange(InferStandaloneLoseConditions(prompt));

        var coordinates = new (int X, int Y, int Direction)[]
        {
            (6, 18, 0), (5, 19, 0), (7, 19, 0), (6, 20, 0), (8, 18, 3),
            (6, 5, 2), (4, 7, 2), (8, 7, 2), (3, 9, 2), (9, 9, 2), (5, 11, 2), (7, 11, 2), (4, 13, 2), (8, 13, 2)
        };
        for (var i = 0; i < design.Roles.Count && i < coordinates.Length; i++)
        {
            var role = design.Roles[i];
            var side = role.Faction == "ally" ? "ally" : role.Faction == "friendly" ? "friendly" : "enemy";
            design.Battle.Units.Add(new BattleDeploymentUnit
            {
                RoleKey = role.Key,
                Side = side,
                X = coordinates[i].X,
                Y = coordinates[i].Y,
                Direction = coordinates[i].Direction,
                AiPolicy = side == "enemy" ? 2 : 0,
                LevelBonus = role.Level
            });
        }
    }

