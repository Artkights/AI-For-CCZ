    private static void AddStandaloneResourcePlan(StandaloneScenarioDesign design)
    {
        design.Resources.Style = "classic CCZ 6.5 pixel-compatible assets";
        foreach (var role in design.Roles.Take(6))
        {
            design.Resources.Needs.Add(new ScenarioResourceNeed { Kind = "face", Target = role.Name, Description = role.Name + " portrait" });
            design.Resources.Needs.Add(new ScenarioResourceNeed { Kind = "r_actor", Target = role.Name, Description = role.Name + " R actor" });
            design.Resources.Needs.Add(new ScenarioResourceNeed { Kind = "s_unit", Target = role.Name, Description = role.Name + " S unit" });
        }

        foreach (var need in design.Resources.Needs)
        {
            need.PlaceholderAllowed = false;
        }

        design.Resources.Needs.Add(new ScenarioResourceNeed { Kind = "map", Target = design.Battle.MapId, Description = design.Battle.MapTheme + " map", PlaceholderAllowed = false });
        design.Resources.Needs.Add(new ScenarioResourceNeed { Kind = "icon", Target = design.Title, Description = design.Title + " icon", PlaceholderAllowed = false });
    }

    private static ScenarioEventAction Dialog(string actorKey, string text)
        => new() { Kind = "dialog", ActorKey = actorKey, Text = text };

    private static ScenarioEventAction Text(string text)
        => new() { Kind = "text", Text = text };

    private static void BindStandaloneSlots(ModAvailableSlotsResult slots, StandaloneScenarioDesign design)
    {
        var personIds = PickIds(slots, "person", design.Roles.Count);
        var jobIds = PickIds(slots, "job_detail", Math.Min(design.Roles.Count, 8));
        var faceIds = PickImageIds(slots, "Face", design.Roles.Count);
        var rImages = PickImageIds(slots, "Pmapobj", design.Roles.Count).Select(id => Math.Max(0, (id - 1) / 2)).Distinct().ToList();
        var sImages = PickImageIds(slots, "Unit_mov", design.Roles.Count);
        for (var i = 0; i < design.Roles.Count; i++)
        {
            var role = design.Roles[i];
            if (i < personIds.Count) role.PersonId = personIds[i];
            if (jobIds.Count > 0) role.JobId = jobIds[Math.Min(i, jobIds.Count - 1)];
            if (i < faceIds.Count) role.FaceImageNumber = faceIds[i];
            if (i < rImages.Count) role.RImageId = rImages[i];
            if (i < sImages.Count) role.SImageId = sImages[i];
        }

        design.RScenarioId = FirstNonEmpty(PickKeys(slots, "r_scenario", 1).FirstOrDefault(), design.RScenarioId, "R_AUTO");
        design.SScenarioId = FirstNonEmpty(PickKeys(slots, "s_scenario", 1).FirstOrDefault(), design.SScenarioId, "S_AUTO");
    }

    private void AddStandaloneTableUpdates(
        CczProject project,
        IReadOnlyList<HexTableDefinition> tables,
        ModPackage package,
        StandaloneScenarioDesign design)
    {
        var modRoles = design.Roles.Select(role => new ModDesignRole
        {
            Name = role.Name,
            Faction = role.Faction,
            Job = role.Job,
            Personality = role.Personality,
            GameplayRole = role.GameplayRole
        }).ToList();
        var engine = _engineProfiles.Detect(project);
        AddRoleTableUpdates(project, tables, package, engine.TableHints.PersonTable, modRoles, design.Roles.Select(role => role.PersonId).Where(id => id > 0).ToList());
        AddJobTableUpdates(project, tables, package, engine.TableHints.DetailedJobTable, modRoles, design.Roles.Select(role => role.JobId).Where(id => id > 0).Distinct().ToList());
    }

    private static void AddStandaloneEffectPackages(ModPackage package, StandaloneScenarioDesign design)
    {
        if (package.SlotPlan.EffectIds.Count == 0 || package.SlotPlan.ItemIds.Count == 0) return;
        var effectId = package.SlotPlan.EffectIds[0];
        var itemId = package.SlotPlan.ItemIds[0];
        package.EffectPackages.Add(new EffectPackage
        {
            PackageId = package.Metadata.PackageId + "-standalone-effect",
            Domain = "item",
            EffectId = effectId,
            Name = TrimGbk("Stage Reward", 16),
            Description = TrimGbk("Standalone stage generated effect.", 64),
            EffectValue = 8,
            Bindings =
            {
                new EffectPackageBinding { Kind = "item", RowId = itemId, ItemId = itemId, EffectValue = 8, Note = "Standalone generated effect." }
            },
            SourcePrompt = design.SourcePrompt,
            BackupNote = "Standalone scenario effect."
        });
    }

    private static void AddStandaloneScenarioPatches(CczProject project, ModPackage package, StandaloneScenarioDesign design)
    {
        var rsRoot = Path.Combine(project.GameRoot, "RS");
        var rPath = ExistingScenarioPath(rsRoot, "R") ?? Path.Combine("RS", FirstNonEmpty(design.RScenarioId, "R_AUTO") + ".eex");
        var sPath = ExistingScenarioPath(rsRoot, "S") ?? Path.Combine("RS", FirstNonEmpty(design.SScenarioId, "S_AUTO") + ".eex");
        package.ScenarioPatches.Add(BuildStandaloneRScenarioPatch(package, design, rPath));
        package.ScenarioPatches.Add(BuildStandaloneSScenarioPatch(package, design, sPath));
    }

    private static ModScenarioPatch BuildStandaloneRScenarioPatch(ModPackage package, StandaloneScenarioDesign design, string relativePath)
    {
        var commands = new List<ModScenarioCommandDraft>
        {
            TextCommand(0x14, "[" + design.Title + "] " + design.Synopsis)
        };
        foreach (var node in design.EventGraph.Nodes.Where(node => node.Kind is "intro" or "epilogue"))
        {
            commands.Add(TextCommand(0x14, "[" + node.Title + "]"));
            commands.AddRange(node.Actions.Select(BuildScenarioActionCommand));
        }

        commands.Add(TextCommand(0x19, "Objective: " + design.Battle.Objective));
        commands.Add(Command(0x11, Word(ParseScenarioNumber(design.SScenarioId)), Word(0)));
        commands[^1].Note = "typed_event:jump_to_battle target=" + design.SScenarioId;
        return new ModScenarioPatch
        {
            PatchId = package.Metadata.PackageId + "-standalone-r",
            RelativePath = relativePath,
            Note = "Standalone R event graph.",
            Operations =
            {
                new ModScenarioPatchOperation
                {
                    Operation = "append_command",
                    SceneIndex = 1,
                    SectionIndex = 1,
                    Commands = commands,
                    Note = "force_open standalone R event graph."
                }
            }
        };
    }

    private static ModScenarioPatch BuildStandaloneSScenarioPatch(ModPackage package, StandaloneScenarioDesign design, string relativePath)
    {
        var commands = new List<ModScenarioCommandDraft>
        {
            TextCommand(0x14, "Battle objective: " + design.Battle.Objective),
            TextCommand(0x19, "Win: " + string.Join("; ", design.Battle.WinConditions)),
            TextCommand(0x1A, "Lose: " + string.Join("; ", design.Battle.LoseConditions)),
            BuildDeploymentCommand(0x44, design.Battle.Units.Where(unit => unit.Side == "ally"), design),
            BuildDeploymentCommand(0x46, design.Battle.Units.Where(unit => unit.Side == "friendly"), design),
            BuildDeploymentCommand(0x47, design.Battle.Units.Where(unit => unit.Side == "enemy"), design)
        };

        foreach (var node in design.EventGraph.Nodes.Where(node => node.Kind is not "intro" and not "epilogue"))
        {
            commands.Add(BuildScenarioEventTriggerCommand(node, design));
            commands[^1].Note = "typed_event:" + node.Kind + " id=" + node.Id + " trigger=" + node.Trigger;
            commands.AddRange(node.Actions.Select(BuildScenarioActionCommand));
        }

        commands.Add(BuildRewardCommand(design));
        commands.Add(TextCommand(0x14, "Rewards: " + string.Join("; ", design.Rewards)));
        return new ModScenarioPatch
        {
            PatchId = package.Metadata.PackageId + "-standalone-s",
            RelativePath = relativePath,
            Note = "Standalone S battle graph.",
            Operations =
            {
                new ModScenarioPatchOperation
                {
                    Operation = "append_command",
                    SceneIndex = 1,
                    SectionIndex = 1,
                    Commands = commands,
                    Note = "force_open standalone S event graph."
                }
            }
        };
    }

    private static ModScenarioCommandDraft BuildDeploymentCommand(int commandId, IEnumerable<BattleDeploymentUnit> units, StandaloneScenarioDesign design)
    {
        var parameters = new List<ModScenarioParameterDraft>();
        foreach (var unit in units)
        {
            var role = RoleByKey(design, unit.RoleKey);
            parameters.Add(Word(role.PersonId));
            parameters.Add(Word(role.JobId));
            parameters.Add(Word(unit.X));
            parameters.Add(Word(unit.Y));
            parameters.Add(Word(unit.Direction));
            parameters.Add(Word(unit.AiPolicy));
            parameters.Add(Word(role.Level));
            parameters.Add(Word(0));
            parameters.Add(Word(0));
            parameters.Add(Word(0));
            parameters.Add(Word(role.SImageId));
            if (commandId == 0x47) parameters.Add(Word(0));
        }

        var draft = Command(commandId, parameters.ToArray());
        draft.Note = "deployment command " + HexDisplayFormatter.FormatByte((byte)commandId);
        return draft;
    }

    private static ModScenarioCommandDraft BuildScenarioActionCommand(ScenarioEventAction action)
    {
        if (action.Kind.Equals("appear", StringComparison.OrdinalIgnoreCase))
        {
            return Command(0x30, Word(action.Value), Dword(action.X), Dword(action.Y), Word(action.Direction), Word(action.ActionId));
        }

        return TextCommand(0x14, action.Text);
    }

    private static ModScenarioCommandDraft BuildScenarioEventTriggerCommand(ScenarioEventNode node, StandaloneScenarioDesign design)
    {
        var draft = node.Kind.ToLowerInvariant() switch
        {
            "deploy" => Command(0x5A),
            "turn_start" => Command(0x77, Word(ParseFirstInteger(node.Trigger, 1)), Word(0)),
            "area_entered" => Command(0x12, Word(ParseFirstInteger(node.Trigger, 0)), Word(ParseLastInteger(node.Trigger, 0))),
            "unit_defeated" => Command(0x13, Word(RoleByKey(design, "boss").PersonId)),
            "victory" => Command(0x11, Word(ParseScenarioNumber(design.RScenarioId)), Word(1)),
            "defeat" => Command(0x11, Word(ParseScenarioNumber(design.RScenarioId)), Word(2)),
            _ => Command(0x76, Word(0))
        };
        draft.Note = "typed_event:" + node.Kind + ":" + node.Id + ":" + node.Trigger;
        return draft;
    }

    private static ModScenarioCommandDraft BuildRewardCommand(StandaloneScenarioDesign design)
    {
        var draft = Command(0x72, Word(10), Word(design.Rewards.Count));
        draft.Note = "typed_event:reward:" + string.Join("/", design.Rewards);
        return draft;
    }

    private static StandaloneScenarioRole RoleByKey(StandaloneScenarioDesign design, string key)
        => design.Roles.FirstOrDefault(role => role.Key.Equals(key, StringComparison.OrdinalIgnoreCase))
           ?? design.Roles.First();

    private static void AddStandaloneResourcePlans(ModPackage package, StandaloneScenarioDesign design)
    {
        package.SlotPlan.Notes["resource_style"] = design.Resources.Style;
        foreach (var need in design.Resources.Needs)
        {
            package.SlotPlan.Notes["resource:" + need.Kind + ":" + need.Target] = need.Description;
            package.ResourceUpdates.Add(new ModResourceUpdate
            {
                Kind = need.Kind,
                TargetRelativePath = need.Target,
                Operation = "generate_task",
                Note = need.Description + (need.PlaceholderAllowed ? "; placeholder allowed" : "; final asset required")
            });
        }
    }

    private static StandaloneScenarioPlayabilityChecklist BuildStandalonePlayabilityChecklist(StandaloneScenarioDesign design, ModPackage package)
    {
        var checklist = new StandaloneScenarioPlayabilityChecklist
        {
            HasRIntro = design.EventGraph.Nodes.Any(node => node.Kind == "intro"),
            HasRToSJump = package.ScenarioPatches.Any(patch => patch.PatchId.Contains("-r", StringComparison.OrdinalIgnoreCase)),
            HasBattleDeployment = design.Battle.Units.Count >= 10,
            HasVictoryCondition = design.EventGraph.Nodes.Any(node => node.Kind == "victory") && design.Battle.WinConditions.Count > 0,
            HasDefeatCondition = design.EventGraph.Nodes.Any(node => node.Kind == "defeat") && design.Battle.LoseConditions.Count > 0,
            HasBattleEvents = design.EventGraph.Nodes.Count(node => node.Kind is "turn_start" or "unit_defeated" or "area_entered") >= 2,
            HasEpilogue = design.EventGraph.Nodes.Any(node => node.Kind == "epilogue")
        };

        if (!checklist.HasRToSJump) checklist.Risks.Add("R script lacks an S route plan.");
        if (!checklist.HasBattleDeployment) checklist.Risks.Add("S battle deployment is too small.");
        if (!checklist.HasVictoryCondition) checklist.Risks.Add("Missing victory condition.");
        if (!checklist.HasDefeatCondition) checklist.Risks.Add("Missing defeat condition.");
        if (!checklist.HasBattleEvents) checklist.Risks.Add("Too few battle events.");
        if (!checklist.HasEpilogue) checklist.Risks.Add("Missing epilogue.");
        return checklist;
    }

    private static string ExtractStandaloneTitle(string prompt)
    {
        foreach (var marker in new[] { "MOD：", "MOD:", "独立单关：", "独立单关:", "标题：", "标题:" })
        {
            var index = prompt.IndexOf(marker, StringComparison.OrdinalIgnoreCase);
            if (index < 0) continue;
            var tail = prompt[(index + marker.Length)..].Trim();
            var stop = tail.IndexOfAny(['。', '；', ';', '\r', '\n']);
            var title = stop > 0 ? tail[..stop].Trim() : tail.Trim();
            if (!string.IsNullOrWhiteSpace(title)) return title;
        }

        return string.Empty;
    }

